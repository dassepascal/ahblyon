                        <h5>Nos réalisations</h5>


                        <p>
                            • L’école
                            L’école la plus proche était à 15 Km. Une paillote a d’abord été construite, puis un bâtiment en
                            dur regroupant 3 classes, du CP au CM2, ainsi qu’un bureau et une salle de stockage pour les
                            documents. Indépendamment de ce bâtiment, et pour l’hygiène, 3 latrines ont été construites sur
                            le terrain de l’école.
                            L’école a été inaugurée en 2014 en présence des autorités civiles et militaires de la région,
                            des notables, et bien sûr de la population de tout le village.
                            En 2021, l’école accueillait environ 80 enfants, venant de Kongodiana et de 4 autres villages
                            environnants.<br>

                            • La cantine
                            La construction d’un abri pour la cuisine a fait la joie des femmes qui devaient préparer les
                            repas sous le soleil ou la pluie, dans la poussière.<br>

                            • Les logements des instituteurs
                            Les 3 enseignants sont logés chacun dans une maison individuelle sur le terrain de l’école.<br>

                            • Le maraîchage
                            Le projet de soutien au maraîchage, demandé par les villageois, a été accepté par notre
                            association. Il a pu être financé en grande partie par l’un de nos partenaires, l’association
                            Saint Vincent de Paul de la région versaillaise.
                            Le terrain d’un ha a été donné par les cultivateurs du village. Il se divise en 4 parcelles
                            bénéficiant chacune d’un puit. Le tout a été clôturé pour empêcher le passage des animaux. Les
                            récoltes commencent à donner.
                            Avec cette réalisation, les jeunes seront sensibilisés aux enjeux environnementaux et
                            changements climatiques. L’évaluation qualitative et quantitative des productions a démontré que
                            près de la moitié des productions serait utilisée en autoconsommation (amélioration du régime
                            alimentaire familial) et l’autre moitié vendue sur les marchés apportant une nouvelle source de
                            revenus.<br>

